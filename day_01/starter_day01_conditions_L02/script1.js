const mois = String(window.prompt("Veuillez saisir un mois de l'année:"));
if (mois === "septembre" || mois === "octobre" || mois === "novembre") {
    console.log("C'est l'automne")
} else if
(mois === "decembre" || mois === "janvier" || mois === "fevrier") {
console.log("C'est l'hiver")
} else if (mois === "mars" || mois === "avril" || mois === "mai") {
    console.log("C'est le printemps")
} else if
    (mois === "juin" || mois === "juillet" || mois === "aout") {
    console.log("C'est l'été")
}